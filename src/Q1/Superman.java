package Q1;

public class Superman {
    private String name;
    private int heatVisionPower;

    public Superman(String name, int heatVisionPower) {
        this.name = name;
        this.heatVisionPower = heatVisionPower;
    }

    public int getHeatVisionPower(){
        return heatVisionPower;
    }

    public String getName(){
        return name;
    }
}
